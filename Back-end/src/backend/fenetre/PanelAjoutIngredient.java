    private void ajouterIngredient(String nomIngredient) {
        String sql = "INSERT INTO Ingredient (`Nom`) VALUES ('" + nomIngredient + "')";
        PreparedStatement nomOrdre = menuPrincipal.connectPrepared(sql);
        try {
            nomOrdre.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(PanelAjoutIngredient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private boolean checkIngredient(String nomIngredient) {
        String sql = "SELECT COUNT(Nom) FROM Ingredient WHERE Nom = '" + nomIngredient + "'";
        Statement nomOrdre = menuPrincipal.connect();
        try {
            ResultSet resultat = nomOrdre.executeQuery(sql);
            while (resultat.next()) {
                if (resultat.getInt(1) >= 1) {
                    return true;
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(PanelAjoutIngredient.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

